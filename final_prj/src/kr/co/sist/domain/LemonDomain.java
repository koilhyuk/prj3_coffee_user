package kr.co.sist.domain;

public class LemonDomain {
	private int totalLemon, monthlyLemon;

	public LemonDomain() {
	}

	public int getTotalLemon() {
		return totalLemon;
	}

	public void setTotalLemon(int totalLemon) {
		this.totalLemon = totalLemon;
	}

	public int getMonthlyLemon() {
		return monthlyLemon;
	}

	public void setMonthlyLemon(int monthlyLemon) {
		this.monthlyLemon = monthlyLemon;
	}
	
	
}
