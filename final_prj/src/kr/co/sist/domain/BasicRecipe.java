package kr.co.sist.domain;

public class BasicRecipe {
	
	private String gdName,milk,syrup,topping,brType,categoryName,gdImg;
	private int shot,cream,gdPrice;
	
	public String getGdName() {
		return gdName;
	}
	public void setGdName(String gdName) {
		this.gdName = gdName;
	}
	public String getMilk() {
		return milk;
	}
	public void setMilk(String milk) {
		this.milk = milk;
	}
	public String getSyrup() {
		return syrup;
	}
	public void setSyrup(String syrup) {
		this.syrup = syrup;
	}
	public String getTopping() {
		return topping;
	}
	public void setTopping(String topping) {
		this.topping = topping;
	}
	public String getBrType() {
		return brType;
	}
	public void setBrType(String brType) {
		this.brType = brType;
	}
	public int getShot() {
		return shot;
	}
	public void setShot(int shot) {
		this.shot = shot;
	}
	public int getCream() {
		return cream;
	}
	public void setCream(int cream) {
		this.cream = cream;
	}
	public String getCategoryName() {
		return categoryName;
	}
	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
	public int getGdPrice() {
		return gdPrice;
	}
	public void setGdPrice(int gdPrice) {
		this.gdPrice = gdPrice;
	}
	public String getGdImg() {
		return gdImg;
	}
	public void setGdImg(String gdImg) {
		this.gdImg = gdImg;
	}
	

}
